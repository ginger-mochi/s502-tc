use std::fmt::{Debug, Formatter};

pub struct Token {
    pub pos: (String, u32, u32),
    pub val: TokenType,
}

impl Debug for Token {
    fn fmt(&self, f: &mut Formatter) -> std::fmt::Result {
        write!(
            f,
            "{}:{}:{}: {:?}",
            self.pos.0, self.pos.1, self.pos.2, self.val
        )
    }
}

#[derive(PartialEq, Debug)]
pub enum TokenType {
    Adc,
    And,
    Asl,
    Bcc,
    Bcs,
    Beq,
    Bit,
    Bmi,
    Bne,
    Bpl,
    Brk,
    Bvc,
    Bvs,
    Clc,
    Cld,
    Cli,
    Clv,
    Cmp,
    Cpx,
    Cpy,
    Dec,
    Dex,
    Dey,
    Eor,
    Inc,
    Inx,
    Iny,
    Jmp,
    Jsr,
    Lda,
    Ldx,
    Ldy,
    Lsr,
    Nop,
    Ora,
    Pha,
    Php,
    Pla,
    Plp,
    Rol,
    Ror,
    Rti,
    Rts,
    Sbc,
    Sec,
    Sed,
    Sei,
    Sta,
    Stx,
    Sty,
    Tax,
    Tay,
    Tsx,
    Txa,
    Txs,
    Tya,
    Dfb,
    Dfw,
    Equ,
    Hlt,
    Inl,
    Org,
    Sct,
    Txt,
    Period,
    VisFile,
    VisGlobal,
    Colon,
    Pound,
    Comma,
    Lparen,
    Rparen,
    Langle,
    Rangle,
    A,
    X,
    Y,
    Newline,
    Ident(String),
    Str(String),
    Byte(u8),
    Word(u16),
}
